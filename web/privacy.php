            <li><strong>Modify:</strong> Edit or update your business card information at any time</li>
            <li><strong>Delete:</strong> Remove your business cards or delete your entire account</li>
            <li><strong>Export:</strong> Download your data (via vCard format)</li>
            <li><strong>Opt-Out:</strong> Stop receiving service emails (except critical security notifications)</li>
        </ul>
        
