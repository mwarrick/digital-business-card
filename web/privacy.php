        <h2>Introduction</h2>
        <p>ShareMyCard.app ("we," "our," or "us") is committed to protecting your privacy. This Privacy Policy explains how we collect, use, and safeguard your information when you use our ShareMyCard mobile application and web service.</p>
        
        <h2>Information We Collect</h2>
        
        <h3>Information You Provide</h3>
        <ul>
            <li><strong>Email Address:</strong> Used for account creation and passwordless authentication</li>
            <li><strong>Business Card Information:</strong> Name, phone number, email addresses, physical address, job title, company name, bio, and website links</li>
            <li><strong>Images:</strong> Profile photos, company logos, and cover graphics you upload</li>
            <li><strong>Contact Information:</strong> Additional email addresses, phone numbers, and website URLs you add to your business cards</li>
        </ul>
        
        <h3>Automatically Collected Information</h3>
        <ul>
            <li><strong>Usage Data:</strong> Information about how you use the app, including access times and features used</li>
            <li><strong>Device Information:</strong> Device type, operating system version, and unique device identifiers</li>
            <li><strong>Log Data:</strong> Server logs including IP address, access times, and pages viewed</li>
        </ul>
        
        <h3>Analytics and Tracking</h3>
        <p>When you share your business card publicly, we collect analytics data to help you understand card engagement:</p>
        <ul>
            <li><strong>View Tracking:</strong> Number of times your card is viewed via public links</li>
            <li><strong>Click Tracking:</strong> Which contact links (email, phone, website) are clicked</li>
            <li><strong>Download Tracking:</strong> When someone downloads your vCard contact file</li>
            <li><strong>Device Data:</strong> Browser type, device type (mobile/tablet/desktop), and operating system</li>
            <li><strong>Geographic Data:</strong> Approximate country and city based on IP address</li>
            <li><strong>Referrer Data:</strong> Source of traffic to your public card page</li>
            <li><strong>Session IDs:</strong> Temporary identifiers to count unique visitors (stored in cookies for 30 days)</li>
        </ul>
        
        <p><strong>Data Retention:</strong> Detailed analytics events are retained for 30 days, then aggregated into daily statistics. Aggregated data is retained for the lifetime of your account.</p>
        
        <p><strong>Do Not Track:</strong> We respect the Do Not Track (DNT) browser setting. If DNT is enabled, we will not collect analytics data about your visits to public card pages.</p>
        
        <h2>How We Use Your Information</h2>
        <p>We use the information we collect to:</p>
        <ul>
            <li>Provide and maintain the ShareMyCard service</li>
            <li>Authenticate your identity through email verification codes</li>
            <li>Generate and display your digital business cards</li>
            <li>Create QR codes containing your contact information</li>
            <li>Sync your data across devices (iOS app and web)</li>
            <li>Provide analytics insights about your card engagement</li>
            <li>Improve and optimize our services</li>
            <li>Send you verification codes and service-related notifications</li>
            <li>Respond to your requests and provide customer support</li>
        </ul>
        
        <h2>Data Storage and Security</h2>
        
        <h3>Where Your Data is Stored</h3>
        <ul>
            <li><strong>Local Storage:</strong> Your business card data is stored locally on your iOS device using Core Data</li>
            <li><strong>Server Storage:</strong> A copy of your data is stored on our secure servers to enable synchronization and web access</li>
            <li><strong>Image Storage:</strong> Uploaded images are stored on our servers in a secure media directory</li>
        </ul>
        
        <h3>Security Measures</h3>
        <ul>
            <li>All data transmissions use HTTPS encryption</li>
            <li>Passwords are not stored (we use passwordless authentication)</li>
            <li>Email verification codes expire after 10 minutes</li>
            <li>JWT tokens are used for secure API authentication</li>
            <li>Access to your data requires authentication</li>
        </ul>
        
        <h2>Data Sharing and Disclosure</h2>
        <p>We do <strong>NOT</strong>:</p>
        <ul>
            <li>Sell your personal information to third parties</li>
            <li>Share your data with advertisers</li>
            <li>Use your information for marketing purposes without consent</li>
        </ul>
        
        <p>We may share your information only in the following circumstances:</p>
        <ul>
            <li><strong>QR Code Sharing:</strong> When you share your QR code, the recipient can scan it to import your contact information</li>
            <li><strong>Legal Requirements:</strong> If required by law or to respond to legal processes</li>
            <li><strong>Service Providers:</strong> With trusted service providers who assist in operating our service (e.g., hosting providers), bound by confidentiality agreements</li>
        </ul>
        
